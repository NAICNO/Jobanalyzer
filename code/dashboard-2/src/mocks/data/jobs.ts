// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

const NOW = Date.now();

/** Returns an ISO string for `ms` milliseconds ago from now. */
function ago(ms: number): string {
  return new Date(NOW - ms).toISOString();
}

const HOUR = 3_600_000;
const MINUTE = 60_000;

/** Seconds shorthand for time_limit */
function hours(h: number): number {
  return h * 3600;
}

/** KiB from GiB */
function gib(g: number): number {
  return g * 1024 * 1024;
}

/** MiB string for resource descriptor */
function memMiB(gBytes: number): string {
  return `${gBytes * 1024}M`;
}

function gpuUuids(node: string, count: number): string[] {
  const uuids: string[] = [];
  for (let i = 0; i < count; i++) {
    const suffix = node.replace(/[^0-9]/g, '').padStart(3, '0');
    uuids.push(`GPU-gpu${suffix}-${String(i).padStart(2, '0')}-0000-0000-000000000000`);
  }
  return uuids;
}

function resourceString(cpus: number, memGB: number, nodes: number, gpus?: number): string {
  let s = `cpu=${cpus},mem=${memMiB(memGB)},node=${nodes},billing=${cpus}`;
  if (gpus && gpus > 0) {
    s += `,gres/gpu=${gpus}`;
  }
  return s;
}

